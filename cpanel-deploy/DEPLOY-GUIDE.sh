# ============================================================
# 🚀 Amar Deal - cPanel ডিপ্লয়মেন্ট গাইড
# Setup Node.js App ছাড়া রান করার পদ্ধতি
# ============================================================

# ❌ সমস্যা: "Setup Node.js App" কাজ করছে না
# ✅ সমাধান: nohup + Apache Proxy দিয়ে রান করুন

# ============================================================
# ধাপ ১: cPanel Terminal ওপেন করুন
# ============================================================

cPanel Dashboard → Advanced → Terminal (বা SSH)

# ============================================================
# ধাপ ২: প্রজেক্ট আপলোড করুন
# ============================================================

# অপশন A: Git দিয়ে ক্লোন
cd ~
git clone <your-repo-url> amar-deal

# অপশন B: File Manager দিয়ে ZIP আপলোড
# 1. প্রজেক্ট ZIP আপলোড করুন
# 2. Extract করুন amar-deal ফোল্ডারে

# ============================================================
# ধাপ ৩: Dependencies ইনস্টল ও বিল্ড
# ============================================================

cd ~/amar-deal
npm install
npx prisma generate
npx prisma db push
npm run build

# স্ট্যাটিক ফাইল কপি
cp -r .next/static .next/standalone/.next/
cp -r public .next/standalone/

# Logs ডিরেক্টরি তৈরি
mkdir -p logs

# ============================================================
# ধাপ ৪: কনফিগারেশন ফাইল সেটআপ
# ============================================================

# app.js ফাইল তৈরি (Passenger এন্ট্রি পয়েন্ট)
# → cpanel-deploy/app.js ফাইলটি ~/amar-deal/app.js হিসেবে কপি করুন

# .env ফাইল এডিট করুন
nano ~/amar-deal/.env
# → your_cpanel_username পরিবর্তন করুন
# → NEXTAUTH_SECRET পরিবর্তন করুন
# → NEXTAUTH_URL পরিবর্তন করুন

# app.js এ ইউজারনেম পরিবর্তন করুন
nano ~/amar-deal/app.js
# → CPANEL_USERNAME = 'your_cpanel_username' লাইনে আপনার ইউজারনেম দিন

# ============================================================
# ধাপ ৫: .htaccess সেটআপ
# ============================================================

# .htaccess ফাইল কপি করুন public_html এ
cp ~/amar-deal/cpanel-deploy/.htaccess ~/public_html/.htaccess

# ============================================================
# ধাপ ৬: সার্ভার স্টার্ট করুন
# ============================================================

# পদ্ধতি ১: start.sh দিয়ে (সবচেয়ে সহজ)
bash ~/amar-deal/start.sh

# পদ্ধতি ২: ম্যানুয়ালি
cd ~/amar-deal
export $(grep -v '^#' .env | xargs)
nohup node app.js > logs/server.log 2>&1 &

# পদ্ধতি ৩: PM2 দিয়ে (সবচেয়ে স্থিতিশীল)
npm install -g pm2
cd ~/amar-deal
pm2 start app.js --name "amar-deal"
pm2 save
pm2 startup

# ============================================================
# ধাপ ৭: চেক করুন
# ============================================================

# সার্ভার লগ দেখুন
tail -f ~/amar-deal/logs/server.log

# পোর্ট চেক করুন
lsof -i:3000

# লোকালি টেস্ট করুন
curl http://127.0.0.1:3000

# ============================================================
# সার্ভার স্টপ করুন
# ============================================================

bash ~/amar-deal/stop.sh

# অথবা
kill $(cat ~/amar-deal/server.pid)

# ============================================================
# সার্ভার রিস্টার্ট করুন
# ============================================================

bash ~/amar-deal/stop.sh && bash ~/amar-deal/start.sh

# ============================================================
# ❓ সমস্যা হলে
# ============================================================

# Error log চেক
cat ~/amar-deal/logs/server.log
cat ~/error_log
cat ~/logs/yourdomain.com.error.log

# Node.js ভার্সন চেক
node --version  # v18+ লাগবে

# পোর্ট ব্যবহৃত হচ্ছে কিনা চেক
lsof -i:3000

# পোর্ট পরিবর্তন করতে চাইলে
# start.sh এ PORT=3000 পরিবর্তন করুন
# .htaccess এ 3000 পরিবর্তন করুন
